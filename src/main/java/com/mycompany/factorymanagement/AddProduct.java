    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        if (product.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter Prodect Name ");
        } else if (isUpdate) {
            String str = "UPDATE products SET name=? WHERE ID=?";
            try {
                PreparedStatement ps = con.connection.prepareStatement(str);
                ps.setString(1, product.getText().trim());
                ps.setInt(2, productData.getId());
                ps.execute();
                JOptionPane.showMessageDialog(null, "updated sucessfully");
                ProductTable employee = new ProductTable(data);
                employee.setVisible(true);
                this.dispose();

            } catch (SQLException ex) {
                Logger.getLogger(AddProduct.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            String sql = "INSERT INTO products (name) VALUES(?)";
            try {
                PreparedStatement ps = con.connection.prepareStatement(sql);
                ps.setString(1, product.getText().trim());
                ps.execute();
//                    System.out.println("Added employee sucessfully");
                JOptionPane.showMessageDialog(null, "added sucessfully");
//                 
                ProductTable employee = new ProductTable(data);
                employee.setVisible(true);
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(AddEmployee.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
       
    }//GEN-LAST:event_addActionPerformed

